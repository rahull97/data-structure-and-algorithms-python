# create list
li = [10, 20, 30, 'Hello']
print(li)
print("----------------------")

# access elements of list
li = [10, 20, 30, 'Hello']
print(li[1])
print(li[-1])
print("----------------------")

# traverse through list
li = [10, 20, 30, 'Hello']

for el in li:
    print(el)

print("----------------------")
